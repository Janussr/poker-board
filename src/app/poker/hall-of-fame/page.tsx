//   wins: number;
// }

export default function HallOfFamePage() {
  const [hallOfFame, setHallOfFame] = useState<HallOfFameEntry[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchData = async () => {
      try {
        const data = await getHallOfFame();
        setHallOfFame(data);
      } catch (err) {
        console.error("Failed to fetch Hall of Fame:", err);
      } finally {
        setLoading(false);
      }
    };

    fetchData();
  }, []);

  const getMedal = (index: number) => {
    if (index === 0) return "ü•á";
    if (index === 1) return "ü•à";
    if (index === 2) return "ü•â";
    return "üéñÔ∏è";
  };

  return (
    <Box sx={{ maxWidth: 600, mx: "auto", mt: 6, px: 2 }}>
      <Typography
        variant="h4"
        sx={{ mb: 3, textAlign: "center", fontWeight: "bold" }}
      >
        üèÜ Hall of Fame
      </Typography>

      <Card sx={{ borderRadius: 3, boxShadow: 4 }}>
        <CardContent>
          {loading ? (
            <Stack alignItems="center" py={4}>
              <CircularProgress />
            </Stack>
          ) : (
            <List>
              {hallOfFame.map((entry, i) => (
                <Box key={entry.playerName}>
                  <ListItem>
                    <ListItemText
                      primary={
                        <Stack direction="row" spacing={1} alignItems="center">
                          <Typography variant="h6">
                            {getMedal(i)} {entry.playerName}
                          </Typography>
                        </Stack>
                      }
                      secondary={`${entry.wins} wins`}
                    />
                    <Chip
                      label={`${entry.wins}`}
                      color={i === 0 ? "success" : i === 1 ? "warning" : "default"}
                    />
                  </ListItem>
                  {i < hallOfFame.length - 1 && <Divider />}
                </Box>
              ))}
            </List>
          )}
        </CardContent>
      </Card>
    </Box>
  );
}